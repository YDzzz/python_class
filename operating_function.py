#operating function
x = 10
y = 3
# x的绝对值
print ("abs(x) = ", abs(x))
# x除以y的商和余数
print ("divmod(x, y) = ", divmod(x, y))
# x的y次方取模1000
print ("pow(x, y, 1000) = ", pow(x, y, 1000))
# x保留d位小数
print ("round(x, d) = ", round(x, y))
print ("max(x, y) = ", max(x, y))
print ("min(x, y) = ", min(x, y))
print ("int(x) = ", int(x))
print ("float(x) = ", float(x))
print ("complex(x) = ", complex(x))


